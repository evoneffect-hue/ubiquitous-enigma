// Вспомогательная функция для получения описания товара
function getProductDescription(productName) {
    if (productName.includes('Процессор')) return 'Высокопроизводительный процессор';
    if (productName.includes('Материнская')) return 'Надежная материнская плата';
    if (productName.includes('Видеокарта')) return 'Мощная игровая видеокарта';
    if (productName.includes('SSD')) return 'Быстрый накопитель';
    if (productName.includes('Блок питания')) return 'Надежный блок питания';
    if (productName.includes('Кулер')) return 'Эффективная система охлаждения';
    if (productName.includes('Монитор')) return 'Качественный монитор';
    if (productName.includes('Клавиатура')) return 'Удобная клавиатура';
    if (productName.includes('Мышь')) return 'Точная компьютерная мышь';
    if (productName.includes('Наушники')) return 'Качественные наушники';
    return 'Отличный товар для вашего компьютера';
}

