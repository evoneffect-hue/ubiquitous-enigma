// Обновление состояния кнопок избранного
function updateFavoriteButtons(favorites) {
    const favoriteBtns = document.querySelectorAll('.favorite-btn');
    favoriteBtns.forEach(btn => {
        try {
            const productData = JSON.parse(btn.dataset.product);
            const isFavorite = favorites.some(fav => fav.id === productData.id);
            if (isFavorite) {
                btn.classList.add('active');
                btn.innerHTML = '♥';
            } else {
                btn.classList.remove('active');
                btn.innerHTML = '♡';
            }
        } catch (e) {
            console.error('Ошибка парсинга данных товара:', e);
        }
    });
}

