// Функция для переключения избранного со страницы товара
function toggleFavoriteFromDetail() {
    if (!currentUser) {
        showToast('Пожалуйста, войдите в аккаунт', 'warning', 'Требуется авторизация');
        openModal();
        return;
    }

    if (!currentProduct) return;

    const userFavorites = JSON.parse(localStorage.getItem(`favorites_${currentUser.username}`)) || [];
    const index = userFavorites.findIndex(fav => fav.id === currentProduct.id);

    if (index === -1) {
        userFavorites.push(currentProduct);
        showToast(`${currentProduct.name} добавлен в избранное`, 'success', 'Товар в избранном');
        document.querySelector('.detail-favorite-btn').innerHTML = '♥ В избранном';
    } else {
        userFavorites.splice(index, 1);
        showToast(`${currentProduct.name} удален из избранного`, 'info', 'Товар удален');
        document.querySelector('.detail-favorite-btn').innerHTML = '♡ Добавить в избранное';
    }

    localStorage.setItem(`favorites_${currentUser.username}`, JSON.stringify(userFavorites));

    // Обновляем кнопки на карточках в каталоге
    updateFavoriteButtons(userFavorites);
}

