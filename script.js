// Переключение между входом и регистрацией
function toggleAuthMode() {
    isLoginMode = !isLoginMode;
    const title = document.getElementById('modalTitle');
    const submitBtn = document.getElementById('authForm').querySelector('input[type="submit"]');
    const switchText = document.querySelector('.switch-mode');

    if (isLoginMode) {
        title.textContent = 'Вход в аккаунт';
        submitBtn.value = 'Войти';
        switchText.innerHTML = 'У вас нет аккаунта? <strong>Зарегистрироваться</strong>';
    } else {
        title.textContent = 'Регистрация';
        submitBtn.value = 'Зарегистрироваться';
        switchText.innerHTML = 'Уже есть аккаунт? <strong>Войти</strong>';
    }
}

// Обработка формы авторизации/регистрации
document.getElementById('authForm').addEventListener('submit', function (e) {
    e.preventDefault();
    const username = document.getElementById('username').value.trim();
    const email = document.getElementById('email').value.trim();
    const password = document.getElementById('password').value;

    if (isLoginMode) {
        const user = users.find(u => u.username === username && u.password === password);
        if (user) {
            currentUser = user;
            localStorage.setItem('currentUser', JSON.stringify(currentUser));
            updateUIForLoggedInUser();
            loadUserFavorites();
            closeModal();
            showToast(`Добро пожаловать, ${username}!`, 'success', 'Вход выполнен');
            document.getElementById('authMessage').textContent = `Добро пожаловать, ${username}!`;
        } else {
            showToast('Неверный логин или пароль', 'error', 'Ошибка входа');
            document.getElementById('authMessage').innerHTML = '<span style="color:#ff6b6b">Неверный логин или пароль!</span>';
        }
    } else {
        if (users.find(u => u.username === username || u.email === email)) {
            showToast('Пользователь уже существует', 'error', 'Ошибка регистрации');
            document.getElementById('authMessage').innerHTML = '<span style="color:#ff6b6b">Пользователь уже существует!</span>';
            return;
        }
        const newUser = { username, email, password };
        users.push(newUser);
        localStorage.setItem('users', JSON.stringify(users));
        currentUser = newUser;
        localStorage.setItem('currentUser', JSON.stringify(currentUser));
        updateUIForLoggedInUser();
        loadUserFavorites();
        closeModal();
        showToast(`Регистрация успешна, ${username}!`, 'success', 'Добро пожаловать');
        document.getElementById('authMessage').textContent = `Регистрация успешна, ${username}!`;
    }
});

